=head1 NAME

DataDist - Convention for distribution that primarily contains/shares data

=head1 VERSION

This document describes version 0.0.0 of DataDist (from Perl distribution DataDist), released on 2016-12-02.

=head1 DESCRIPTION

B<CURRENTLY EMPTY, NAME GRAB>

C<DataDist::> is a namespace meant for Perl distributions that mainly
contain/share data instead of code. This module, C<DataDist>, provides
convention for such distributions.

