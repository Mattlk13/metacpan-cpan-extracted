use constant SIGNATURE => "common";

use vars qw($VERSION);
$VERSION = '1.00';

sub build { # Building
    my $self = shift;

    my $rplc = $self->{rplc};
    $rplc->{TODO} = "TODO";

    $self->maybe::next::method();
    return 1;
}
sub dirs { # Directories and permissions as array of hashs
    my $self = shift;
    $self->{subdirs}{(SIGNATURE)} = [
        {
            path => 'conf',
            mode => 0755,
        },
        {
            path => 'css',
            mode => 0755,
        },
        {
            path => 'img',
            mode => 0755,
        },
        {
            path => 'inc',
            mode => 0755,
        },
        {
            path => 'js',
            mode => 0755,
        },
        {
            path => 'lib',
            mode => 0755,
        },
        {
            path => 'src',
            mode => 0755,
        },
        {
            path => 't',
            mode => 0755,
        },
    ];
    $self->maybe::next::method();
    return 1;
}
sub pool { # Multipart pool of files
    my $self = shift;
    my $pos =  tell DATA;
    my $data = scalar(do { local $/; <DATA> });
    seek DATA, $pos, 0;
    $self->{pools}{(SIGNATURE)} = $data;
    $self->maybe::next::method();
    return 1;
}

1;
