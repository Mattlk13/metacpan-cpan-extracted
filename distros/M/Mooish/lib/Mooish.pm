=head1 NAME

Mooish - Modules related to the Mo* modules family (Mo, Moo, Moose, Mouse)

=head1 VERSION

This document describes version 0.01 of Mooish (from Perl distribution Mooish), released on 2015-07-26.

=head1 DESCRIPTION

The C<Mooish> and C<MooishX> namespaces are reserved for modules related to the
Mo* modules family (Mo, Moo, Moose, Mouse, and possibly others) instead of
certain specific implementation.

