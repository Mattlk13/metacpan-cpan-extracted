# -*- cperl -*-
# ABSTRACT: LaTeX Integernumber object


use strict;
use warnings;
package SpeL::Object::Integernumber;

use parent 'Exporter';
use Carp;

use SpeL::I18n;

use Data::Dumper;



sub read {
  my $self = shift;
  my ( $level ) = @_;
  my $sign = '';
  if ( exists $self->{Sign} ) {
    $sign .= $self->{Sign} eq '+' ? $SpeL::I18n::lh->maketext('+') . ' ' : '';
    $sign .= $self->{Sign} eq '-' ? $SpeL::I18n::lh->maketext('-') . ' ' : '';
  }
  return $sign . $self->{Value};
}

1;

