# -*- cperl -*-
package SpeL::Object::Fraction;

# ABSTRACT: LaTeX fraction object


use strict;
use warnings;

use parent 'Exporter';
use Carp;

use SpeL::Object::MathUnit;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  return $SpeL::I18n::lh->maketext( 'Div',
					     $self->{num}->read( $level + 1 ),
					     $self->{den}->read( $level + 1 ) );
}

1;

__END__

