# -*- cperl -*-
# ABSTRACT: LaTeX  matrix object


use strict;
use warnings;
package SpeL::Object::Matrix;

use SpeL::Object::Option;
use SpeL::Object::MathElementList;
use SpeL::I18n;

use parent 'Exporter';
use Carp;

use Data::Dumper;




sub read {
  my $self = shift;
  my ( $level ) = @_;
  
  return
      $SpeL::I18n::lh->maketext( $self->{matrixtag} ) .
      join( ', ' .
	    $SpeL::I18n::lh->maketext( 'And' ) .
	    ', ',
	    map { $_->read( $level + 1 ) } @{$self->{MathUnit}} ) . ', ';
}

1;

__END__

