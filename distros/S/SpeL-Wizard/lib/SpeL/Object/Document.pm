package SpeL::Object::Document;

use SpeL::Object::ElementList;

use parent 'Exporter';
use Carp;

use Data::Dumper;


sub new {
  my $class = shift;
  my ( $self ) = @_;
  $self = {} unless $self;
  bless $self, $class;
}



1;

