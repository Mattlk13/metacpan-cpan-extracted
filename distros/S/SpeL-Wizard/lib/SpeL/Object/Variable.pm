# -*- cperl -*-
# ABSTRACT: LaTeX variable (math) object


use strict;
use warnings;
package SpeL::Object::Variable;

use parent 'Exporter';
use Carp;

use SpeL::I18n;

use Data::Dumper;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  my $line;
  if( exists $self->{Greek} ) {
    $self->{Greek} =~ s/\\(?:mit|mup)?//;
    $self->{Greek} =~ s/\\(?:var)?//;
    $line = $SpeL::I18n::lh->maketext( lc( $self->{Greek} ) );
  }
  else {
    $line = '"' . $self->{Alphabet} . '"';
  }
  if ( exists $self->{Subscript} ) {
    $line .= $self->{Subscript}->read( $level + 1 );
  }
  return $line;
}

1;

