# -*- cperl -*-
# ABSTRACT: LaTeX variable (math) object


use strict;
use warnings;
package SpeL::Object::Variable;

use parent 'Exporter';
use Carp;

use SpeL::I18n;

use Data::Dumper;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  exists $self->{Greek} and do {
    $self->{Greek} =~ s/\\(?:var)?//;
    return $SpeL::I18n::lh->maketext( $self->{Greek} );
  };
  return $self->{Alphabet};
}

1;

__END__

