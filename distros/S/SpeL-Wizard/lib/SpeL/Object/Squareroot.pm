# -*- cperl -*-
# ABSTRACT: LaTeX square (or n-) root


use strict;
use warnings;
package SpeL::Object::Squareroot;

use parent 'Exporter';
use Carp;

use SpeL::I18n;
use Data::Dumper;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  my $N = 2;
  if ( exists $self->{N} ) {
    $N = $self->{N}->read( $level + 1 );
    $N =~ s/^\s*(\w+)\s*$/$1/;
  }

  return $SpeL::I18n::lh->maketext('Squareroot',
					    $N,
					    $self->{Argument}->read( $level + 1 ) ) . ', ';
}

1;

__END__

