# -*- cperl -*-
# ABSTRACT: LaTeX expression object


package SpeL::Object::Interval;

use SpeL::Object::Expression;

use SpeL::I18n;

use parent 'Exporter';
use Carp;

use Data::Dumper;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  my $type =
      ( $self->{Intervalstart} eq '[' ? 'c' : 'o' )
      .
      ( $self->{Intervalend} eq ']' ? 'c' : 'o' );
  
  return $SpeL::I18n::lh->
      maketext( 'Interval',
		$type,
		$self->{left}->read( $level + 1 ),
		$self->{right}->read( $level + 1 ) );
}

1;

__END__

