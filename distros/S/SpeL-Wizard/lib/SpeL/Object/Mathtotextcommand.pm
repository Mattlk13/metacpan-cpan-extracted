# -*- cperl -*-
package SpeL::Object::Mathtotextcommand;
# ABSTRACT: SpeL command object


use strict;
use warnings;

use parent 'Exporter';
use Carp;

use SpeL::Object::Option;
use SpeL::Object::ElementList;
use SpeL::Object::Command;
use SpeL::I18n;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  # this is a very dirty trick but it avoids having to duplicate
  # the entire function.
  return SpeL::Object::Command::read( $self, $level );
}

1;

__END__

