# -*- cperl -*-
# ABSTRACT: LaTeX overbrace object


package SpeL::Object::Overbrace;

use parent 'Exporter';
use Carp;

use SpeL::I18n;
use Data::Dumper;
use SpeL::Object::MathElement;
use SpeL::Object::Limitscommand;




sub read {
  my $self = shift;
  my ( $level ) = @_;

  $SpeL::I18n::lh->maketext( 'Overbrace',
			     $self->{group}->read( $level + 1 ),
			     $self->{over}->read( $level + 1 ) );
}


1;

__END__

