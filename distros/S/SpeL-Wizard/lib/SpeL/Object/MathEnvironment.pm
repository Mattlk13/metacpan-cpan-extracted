# -*- cperl -*-
# ABSTRACT: LaTeX  mathenvironment object


use strict;
use warnings;
package SpeL::Object::MathEnvironment;

use SpeL::Object::Option;
use SpeL::Object::MathElementList;
use SpeL::I18n;

use parent 'Exporter';
use Carp;

use Data::Dumper;




sub read {
  my $self = shift;
  my ( $level ) = @_;

  return
    join( ', ',
	  map { $_->read( $level + 1 ) } @{$self->{MathUnit}} );
}

1;

