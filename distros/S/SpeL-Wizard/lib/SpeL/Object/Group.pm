# -*- cperl -*-
# ABSTRACT: LaTeX group object


use strict;
use warnings;
package SpeL::Object::Group;

use parent 'Exporter';
use Carp;

use Data::Dumper;

use SpeL::Object::ElementList;



sub read {
  my $self = shift;
  my ( $level ) = @_;

  return $self->{ElementList}->read( $level + 1 );
}

1;

