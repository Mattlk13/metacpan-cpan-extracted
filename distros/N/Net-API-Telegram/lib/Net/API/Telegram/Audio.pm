package Net::API::Telegram::Audio;
BEGIN
{
	use strict;
	use parent qw( Net::API::Telegram::Generic );
    our( $VERSION ) = '0.1';
};

sub download { return( shift->_download( @_ ) ); }

sub duration { return( shift->_set_get_number( 'duration', @_ ) ); }

sub file_id { return( shift->_set_get_scalar( 'file_id', @_ ) ); }

sub file_size { return( shift->_set_get_number( 'file_size', @_ ) ); }

sub mime_type { return( shift->_set_get_scalar( 'mime_type', @_ ) ); }

sub performer { return( shift->_set_get_scalar( 'performer', @_ ) ); }

sub thumb { return( shift->_set_get_object( 'thumb', 'Net::API::Telegram::PhotoSize', @_ ) ); }

sub title { return( shift->_set_get_scalar( 'title', @_ ) ); }

1;

__END__

