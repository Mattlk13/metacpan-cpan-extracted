=head1 DESCRIPTION

L<Net::API::Telegram::PhotoSize> is a Telegram Message Object as defined here L<https://core.telegram.org/bots/api#photosize>

This module has been automatically generated from Telegram API documentation by the script scripts/telegram-doc2perl-methods.pl.

