=head1 VERSION

This document describes version 0.001 of Acme::CPANLists::Import::BKB (from Perl distribution Acme-CPANLists-Import-BKB), released on 2017-03-27.

=head1 DESCRIPTION

This distribution contains module lists that are imported from Ben Bullock's
(CPANID:BKB) website posts located at L<https://www.lemoda.net/>. This
distribution is created to improve the "mentions cloud".

