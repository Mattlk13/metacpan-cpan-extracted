=head1 NAME

Acme::CPANLists::Import::NEILB - CPANLists imported from http://neilb.org/reviews/

=head1 VERSION

This document describes version 0.04 of Acme::CPANLists::Import::NEILB (from Perl distribution Acme-CPANLists-Import-NEILB), released on 2016-02-21.

=head1 DESCRIPTION

This distribution contains module lists that are imported from Neil Bowers'
(CPANID:NEILB) module review articles located at L<http://neilb.org/reviews/>.
This distribution is created to improve the "mentions cloud".

