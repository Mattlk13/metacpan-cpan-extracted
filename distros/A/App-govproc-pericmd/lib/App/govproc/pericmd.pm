=head1 VERSION

This document describes version 0.001 of App::govproc::pericmd (from Perl distribution App-govproc-pericmd), released on 2019-05-22.

=head1 SYNOPSIS

See the included script L<govproc-pericmd>.

=head1 DESCRIPTION

This distribution carries L<govproc-pericmd>, the L<govproc> CLI utility based
on L<Perinci::CmdLine>, mainly for testing. Normal users should still use the
official B<govproc> from L<Proc::Govern> that is lighter and has fewer
dependencies.

